enum AsciiFont { block, banner, standard, slant, lean, script }

class TextGenerator {
  static String generate(String text, {AsciiFont font = AsciiFont.standard, int width = 80}) {
    switch (font) {
      case AsciiFont.block:
        return _generateBlock(text);
      case AsciiFont.banner:
        return _generateBanner(text);
      case AsciiFont.standard:
        return _generateStandard(text);
      case AsciiFont.slant:
        return _generateSlant(text);
      case AsciiFont.lean:
        return _generateLean(text);
      case AsciiFont.script:
        return _generateScript(text);
    }
  }

  static String _generateBlock(String text) {
    final lines = List.filled(5, '');
    final upper = text.toUpperCase();

    for (int i = 0; i < upper.length; i++) {
      final char = upper[i];
      if (char == ' ') {
        for (int j = 0; j < 5; j++) {
          lines[j] += '     ';
        }
        continue;
      }

      final blockChar = _blockChars[char] ?? ['     ', '     ', '     ', '     ', '     '];
      for (int j = 0; j < 5; j++) {
        lines[j] += blockChar[j] + ' ';
      }
    }

    return lines.join('\n');
  }

